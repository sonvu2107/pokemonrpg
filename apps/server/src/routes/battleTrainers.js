import express from 'express'
import BattleTrainer from '../models/BattleTrainer.js'

const router = express.Router()
const BATTLE_TRAINERS_CACHE_TTL_MS = 30 * 1000

let battleTrainersCache = {
    expiresAt: 0,
    trainers: [],
}

const activeBattleTrainerFilter = {
    $or: [
        { isActive: true },
        { isActive: { $exists: false } },
        { isActive: null },
    ],
}

// GET /api/battle-trainers - Public list
router.get('/', async (req, res) => {
    try {
        const now = Date.now()
        if (battleTrainersCache.expiresAt > now && Array.isArray(battleTrainersCache.trainers)) {
            return res.json({ ok: true, trainers: battleTrainersCache.trainers })
        }

        const trainerRows = await BattleTrainer.find(activeBattleTrainerFilter)
            .sort({ orderIndex: 1, createdAt: 1 })
            .populate('team.pokemonId', 'name pokedexNumber imageUrl sprites forms defaultFormId baseStats rarity types')
            .populate('prizePokemonId', 'name pokedexNumber imageUrl sprites forms defaultFormId')
            .populate('prizeItemId', 'name imageUrl type rarity')
            .lean()

        const trainers = trainerRows.map((entry) => {
            if (!entry?.autoGenerated) return entry
            return {
                ...entry,
                moonPointsReward: 0,
            }
        })

        battleTrainersCache = {
            trainers,
            expiresAt: now + BATTLE_TRAINERS_CACHE_TTL_MS,
        }

        res.json({ ok: true, trainers })
    } catch (error) {
        console.error('GET /api/battle-trainers error:', error)
        res.status(500).json({ ok: false, message: 'Lỗi máy chủ' })
    }
})

export default router
